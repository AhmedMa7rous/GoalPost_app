import UIKit

extension UIButton{
    
    func setSelectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.001267400841, green: 0.7769988551, blue: 0.01221280891, alpha: 1)
    }
    
    func setDeselectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.6314300994, green: 0.8902797461, blue: 0.4536753257, alpha: 1)
    }
    
}
